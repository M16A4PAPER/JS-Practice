let departments = ["Marketing", "Engineering"];

// Accessing array items
console.log(`First Item: ${departments[0]}`);

// Checking the array length
console.log(`Length: ${departments.length}`);

// Add item to array
departments.push("Human Resources");

// Updated length
console.log(`Length: ${departments.length}`);

